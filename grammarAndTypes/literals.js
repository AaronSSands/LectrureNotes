/*
A literal represents a fixed value that we as developers insert into the code
*/

//string literal
let car = "ford";

//numeric literal
let december = 12;

//boolean literal
let tired = true;


//object literal
let soup = {
    a: "chicken noodle",
    b: "tomato",
    c: "beef and barley"
}

//array literal
let days = ["Monday", "Tuesday", "Wednesday", "Thursday", "Friday"];